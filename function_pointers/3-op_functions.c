#include <stdio.h>
#include "3-calc.h"

/**
 * op_add - sum
 * @a: integer
 * @b: integer
 * Return: check the code
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtraction
 * @a: integer
 * @b: integer
 * Return: check the code
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiply
 * @a: integer
 * @b: integer
 * Return: check the code
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - division
 * @a: integer
 * @b: integer
 * Return: check the code
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - modulus
 * @a: integer
 * @b: integer
 * Return: check the code
 */

int op_mod(int a, int b)
{
	return (a % b);
}
